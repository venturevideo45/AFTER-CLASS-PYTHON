import turtle

t = turtle.Turtle()
sc = turtle.Screen()
sc.bgcolor("white")
sc.title("HOUSE")
sc.setup(400, 400)

t.fillcolor("yellow")
t.begin_fill()
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(90)
t.forward(100)
t.left(180)
t.end_fill()

t.forward(100)

t.fillcolor("red")
t.begin_fill()
t.right(45)
t.forward(70)
t.right(90)
t.forward(70)
t.right(135)
t.end_fill()

t.penup()
t.left(90)
t.forward(100)
t.right(90)
t.forward(40)
t.right(90)
t.pendown()
t.fillcolor("black")
t.begin_fill()
t.forward(25)
t.left(90)
t.forward(15)
t.left(90)
t.forward(25)
t.end_fill()

turtle.hideturtle()
turtle.done()
